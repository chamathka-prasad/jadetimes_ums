			function addNewDocType() {

				var dType = document.getElementById("dType");

				var msg = document.getElementById("docTypeMsg");


				if (dType.value.length == 0) {
					msg.classList = "alert alert-danger";
					msg.innerHTML = "Document Type is Empty";
				} else {
					var formData = new FormData();
					formData.append("document", dType.value);


					fetch(baseUrl + "addNewDocumentProcess.php", {
							method: "POST",
							body: formData,

						})
						.then(function(resp) {

							try {
								let response = resp.json();
								return response;
							} catch (error) {

							}

						})
						.then(function(value) {

							if (value.type == "error") {
								msg.classList = "alert alert-danger";
								msg.innerHTML = value.message;

							} else if (value.type == "success") {
								msg.classList = "alert alert-success";
								msg.innerHTML = value.message;
								dType.value = "";
								setTimeout(() => {
									loadDocumentTypes();
								}, 1000);


							} else {
								msg.classList = "alert alert-danger";
								msg.innerHTML = "Something wrong please try again";

							}

						})
						.catch(function(error) {
							console.log(error);
						});
				}


			}


